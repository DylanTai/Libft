#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int		s1_itr;
	int		s2_itr;

	s1_itr = 0;
	while (s1[s1_itr] != '\0')
		s1_itr++;
	s2_itr = 0;
	while (s2[s2_itr] != '\0')
		s1[s1_itr++] = s2[s2_itr++];
	s1[s1_itr] = '\0';
	return (s1);
}
