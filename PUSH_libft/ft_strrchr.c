#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int size;

	size = 0;
	while (s[size] != '\0')
		size++;
	if (c == 0)
		return ((char *)(s + size));
	while (size >= 0)
	{
		if (s[size] == (char)c)
			return ((char *)(s + size));
		size--;
	}
	return (NULL);
}
